    private void btnSimpan2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpan2ActionPerformed
    
        String jeniskelamin = null;
        if (rbLaki1.isSelected()) {
            jeniskelamin = "Laki-Laki";
        } else if (rbPerempuan1.isSelected()) {
            jeniskelamin = "Perempuan";
        }

        if (txtNama1.getText().trim().equals("") || jeniskelamin == null || cbKelas1.getSelectedItem().equals("----Pilih----") || txtTelepon1.getText().trim().equals("") || txtNIS1.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Data Masih Kosong!", "Ubah Data", JOptionPane.WARNING_MESSAGE);
        } else {
            String sql = "UPDATE siswa SET `Nama Anggota`='" + txtNama1.getText()
                    + "', `Jenis Kelamin`='" + jeniskelamin
                    + "', `Kelas`='" + cbKelas1.getSelectedItem()
                    + "', `Nomor Telepon`='" + txtTelepon1.getText()
                    + "' WHERE `NIS`='" + txtNIS1.getText().trim() + "'";

            try {
                Statement stat = koneksi.createStatement();
                stat.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Data Berhasil di Ubah", "Ubah Data", JOptionPane.INFORMATION_MESSAGE);
                stat.close();
                jFrame1.setVisible(false); 
                tampilData(); 
                
            } catch (Exception exc) {
                System.err.println("Error :" + exc);
            }
    }
    }//GEN-LAST:event_btnSimpan2ActionPerformed

