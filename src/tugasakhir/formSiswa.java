    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String nis     = txtNIS.getText();
        String nama     = txtNama.getText();
         String jeniskelamin = null;
        if (rbLaki.isSelected()) { jeniskelamin = "Laki-Laki";
        } else if
        (rbPerempuan.isSelected()) {jeniskelamin = "Perempuan";
        }
         String kelas = (String) cbKelas.getSelectedItem();
        String telepon     = txtTelepon.getText();
        if (txtNIS.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Mohon Masukkan Data untuk di Input", "PERHATIAN", JOptionPane.WARNING_MESSAGE);
            validasi();
        } else {
            try {
                java.sql.Statement stat = koneksi.createStatement();
                ResultSet data = stat.executeQuery("SELECT * FROM siswa WHERE NIS ='" + txtNIS.getText() + "'");
                if (data.next()) {
                    JOptionPane.showMessageDialog(null, "NIS Sudah Ada", "PERHATIAN", JOptionPane.WARNING_MESSAGE);
                    txtNIS.requestFocus();
                } else {
                    String sql = "INSERT INTO siswa VALUES('" + nis + "'"
                    + ",'" + nama + "'"
                    + ",'" + jeniskelamin + "'"    
                    + ",'" + kelas + "'"
                    + ",'" + telepon + "')";
                    stat.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data Berhasil Di Simpan", "SUKSES", JOptionPane.INFORMATION_MESSAGE);
                }
                stat.close();
                bersihkan();
                tampilData();
                jFrame2.setVisible(false); 
            } catch (Exception exc) {
                System.err.println("Terjadi Kesalahan Simpan :" + exc);
            }
        }       
    }//GEN-LAST:event_btnSimpanActionPerformed

