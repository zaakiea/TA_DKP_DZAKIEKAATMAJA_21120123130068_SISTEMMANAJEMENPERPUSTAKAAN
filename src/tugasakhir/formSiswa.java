    private void tblSiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSiswaMouseClicked
        int baris = tblSiswa.getSelectedRow();
        if (baris != -1) {
            txtNIS1.setText(tblSiswa.getValueAt(baris, 0).toString());
            txtNama1.setText(tblSiswa.getValueAt(baris, 1).toString());
            String jenisKelamin = tblSiswa.getValueAt(baris, 2).toString();
             if (jenisKelamin.equalsIgnoreCase("Laki-Laki")) {
                rbLaki1.setSelected(true);
                rbPerempuan1.setSelected(false);
             } else {
            rbLaki1.setSelected(false);
            rbPerempuan1.setSelected(true);
            }
            cbKelas1.setSelectedItem(tblSiswa.getValueAt(baris, 3).toString());
         
            txtTelepon1.setText(tblSiswa.getValueAt(baris, 4).toString());
        }else {
            JOptionPane.showMessageDialog(null, "Anda belum memilih data yang akan diubah.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_tblSiswaMouseClicked

