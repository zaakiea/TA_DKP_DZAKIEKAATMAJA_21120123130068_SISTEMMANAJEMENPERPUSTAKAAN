    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
   String kode = txtKode.getText();
    String keterangan = txtKet.getText();
    SimpleDateFormat fm = new SimpleDateFormat("yyyy-MM-dd");
    String tanggalkembali = String.valueOf(fm.format(tglKembali.getDate()));

    // Update keterangan dan tgl_kembali di tabel peminjaman
    String sql = "UPDATE peminjaman SET keterangan = ?, tgl_kembali = ? WHERE id_peminjaman = ?";
    
    // Menambahkan jumlah buku dalam tabel buku
    String updateBukuSql = "UPDATE buku SET jumlah = jumlah + 1 WHERE kode_buku = ?";
    
    try {
        // Mulai transaksi
        koneksi.setAutoCommit(false);

        // Update peminjaman
        java.sql.PreparedStatement pst = koneksi.prepareStatement(sql);
        pst.setString(1, keterangan);
        pst.setString(2, tanggalkembali);
        pst.setString(3, txtIdPeminjaman.getText().trim());
        pst.executeUpdate();

        // Update jumlah buku
        java.sql.PreparedStatement pstUpdateBuku = koneksi.prepareStatement(updateBukuSql);
        pstUpdateBuku.setString(1, kode);
        pstUpdateBuku.executeUpdate();

        // Commit transaksi
        koneksi.commit();

        JOptionPane.showMessageDialog(null, "Buku berhasil dikembalikan", "Buku Kembali", JOptionPane.INFORMATION_MESSAGE);

        // Tutup statement
        pst.close();
        pstUpdateBuku.close();
        
        tampilData();
          jFrame1.setVisible(false); 
            tblPengembalian.clearSelection();
    } catch (Exception exc) {
        System.err.println("Error :" + exc);
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

