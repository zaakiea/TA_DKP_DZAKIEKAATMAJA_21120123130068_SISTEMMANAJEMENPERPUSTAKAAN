    private void tampilData() {
         DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Peminjaman");
        model.addColumn("ID Anggota");
        model.addColumn("Nama Anggota");
        model.addColumn("Judul Buku");
        model.addColumn("Kode Buku");
        model.addColumn("Tanggal Peminjaman");
         model.addColumn("Batas Pengembalian");
         model.addColumn("Tanggal Kembalian");
         model.addColumn("Keterangan");
         
        tblPengembalian.setModel(model);
        try {
            java.sql.Statement stat = koneksi.createStatement();
            ResultSet data = stat.executeQuery("SELECT * FROM peminjaman");
            while (data.next()) {
                model.addRow(new Object[]{
                    data.getString("id_peminjaman"),
                    data.getString("id_anggota"),
                    data.getString("nama_peminjam"),
                    data.getString("judul_buku"),
                    data.getString("kode_buku"),
                    data.getString("tgl_peminjaman"),
                    data.getString("batas_pengembalian"),
                     data.getString("tgl_kembali"),
                    data.getString("keterangan")
                });
                tblPengembalian.setModel(model);
            }        
        } catch (Exception e) {
            System.err.println("Terjadi Kesalahan :" + e);
        }      }

